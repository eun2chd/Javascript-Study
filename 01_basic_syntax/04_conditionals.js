// if-else 문
let age = 20;
if (age >= 18) {
  console.log("성인입니다.");
} else {
  console.log("미성년자입니다.");
}

// switch 문
let color = "red";
switch (color) {
  case "red":
    console.log("빨간색입니다.");
    break;
  case "blue":
    console.log("파란색입니다.");
    break;
  default:
    console.log("색상이 지정되지 않았습니다.");
}
