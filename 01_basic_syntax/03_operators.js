// 산술 연산자
let a = 10 + 5;  // 덧셈
let b = 10 * 2;  // 곱셈
let c = 10 % 3;  // 나머지
console.log(a, b, c);

// 비교 연산자
console.log(5 > 3);    // true
console.log(5 == "5"); // true (값만 비교)
console.log(5 === "5");// false (값과 타입 비교)

// 논리 연산자
console.log(true && false); // false
console.log(true || false); // true
console.log(!true);         // false
