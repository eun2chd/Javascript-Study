// for 문
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// while 문
let count = 0;
while (count < 5) {
  console.log(count);
  count++;
}

// do-while 문
let num = 0;
do {
  console.log(num);
  num++;
} while (num < 5);

// 배열 반복
let arr = ["a", "b", "c"];
for (let item of arr) {
  console.log(item);
}
