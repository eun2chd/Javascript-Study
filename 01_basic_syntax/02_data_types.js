// 기본 데이터 타입
let num = 10;       // 숫자
let str = "hello";  // 문자열
let bool = true;    // 불리언
let undef;          // undefined
let nul = null;     // null (비어있음을 의도적으로 표현)

// 객체 타입
let obj = { name: "Alice", age: 25 };  // 객체
let arr = [1, 2, 3];                   // 배열

// typeof로 데이터 타입 확인
console.log(typeof num);  // number
console.log(typeof str);  // string
console.log(typeof obj);  // object
